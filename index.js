const express = require('express');
const cors = require('cors');
const path = require('path');
const axios = require('axios');
  
const app = express();
const PORT = process.env.PORT || 3000;
app.enable("trust proxy");
app.set("json spaces", 2);

// Middleware untuk CORS
app.use(cors());

async function lirics(judul) {
  return new Promise(async (resolve, reject) => {
    axios
      .get("https://www.musixmatch.com/search/" + judul)
      .then(async ({ data }) => {
        const $ = cheerio.load(data);
        const hasil = {};
        let limk = "https://www.musixmatch.com/";
        const link =
          limk + $("div.media-card-body > div > h2").find("a").attr("href");
        await axios.get(link).then(({ data }) => {
          const $$ = cheerio.load(data);
          hasil.thumb =
            "https:" +
            $$(
              "div.col-sm-1.col-md-2.col-ml-3.col-lg-3.static-position > div > div > div",
            )
              .find("img")
              .attr("src");
          $$("div.col-sm-10.col-md-8.col-ml-6.col-lg-6 > div.mxm-lyrics").each(
            function (a, b) {
              hasil.lirik =
                $$(b).find("span > p > span").text() +
                "\n" +
                $$(b).find("span > div > p > span").text();
            },
          );
        });
        resolve(hasil);
      })
      .catch(reject);
  });
}

// Fungsi untuk ragBot
async function ragBot(message) {
  try {
    const response = await axios.post('https://ragbot-starter.vercel.app/api/chat', {
      messages: [{ role: 'user', content: message }],
      useRag: true,
      llm: 'gpt-3.5-turbo',
      similarityMetric: 'cosine'
    });
    return response.data;
  } catch (error) {
    throw error;
  }
}

